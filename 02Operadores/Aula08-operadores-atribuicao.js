//Operadores de atribuição em JS
//Operador de atribuição =
//Atribuição aditiva +=
//Atribuição subtrativa -=
//Atribuição multiplicativa *=
//Atribuição divisiva /=
//Atribuição modular %=

let num = 2 //Atribuição

console.log(num += 2); //aditiva
console.log(num -= 2); //subtrativa
console.log(num *= 2); //multiplicativa
console.log(num /= 2); //divisiva
console.log(num %= 2); //modular