//Operadores lógicos em JS - Retorna sempre true ou false
/*
E &&
OU ||
NÃO !VARIAVELOUELEMENTO
*/
const verdade = true;
const falso = false;

console.log(verdade && falso); //Os dois precisam ser verdadeiros
console.log(verdade || falso); //um dos dois precisa ser verdadeiro
console.log(!verdade); //O contrário do original
console.log(!falso);