//Operadores relacionais em JS (O RETORNO DA OPERAÇÃO LÓGICA É SEMPRE TRUE E FALSE)
/*
IGUALDADE == (NÃO COMPARA TIPOS)
IGUALDADE ESTRITA === (COMPARA TIPOS)
DIFERENÇA !=
DIFERENÇA ESTRITA !==
MENOR QUE <
MAIOR QUE >
MENOR IGUAL <=
MAIOR IGUAL >= 
*/

console.log('3' == 3); //igualdade
console.log('3' === 3); //igualdade estrita
console.log('3' != 3); //diferente
console.log('3' !== 3); //diferença estrita
console.log(3 < 2); //menor
console.log(3 > 2); //maior
console.log(3 <= 2); //menor igual
console.log(3 >= 2); //maior igual